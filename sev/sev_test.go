func TestAlignment(t *testing.T) {
	tests := []struct {
		name     string
		gpaStart uint64
		size     uint32
		product  sgpb.SevProduct_SevProductName
		wantErr  string
	}{
		{
			name:     "Misaligned start",
			gpaStart: 1,
			size:     oabi.PageSize,
			product:  sgpb.SevProduct_SEV_PRODUCT_MILAN,
			wantErr:  "guest data must be of aligned on 0x1000 bytes. Got address 0x1",
		},
		{
			name:     "Misaligned size",
			gpaStart: oabi.PageSize,
			size:     oabi.PageSize + 512,
			product:  sgpb.SevProduct_SEV_PRODUCT_MILAN,
			wantErr:  "guest data must be of multiple of: 0x1000. Given data is size: 0x1200",
		},
		{
			name:     "Unrepresentable address start",
			gpaStart: 0xfffffffffffff000,
			size:     oabi.PageSize,
			product:  sgpb.SevProduct_SEV_PRODUCT_MILAN,
			wantErr:  "address range is larger than the product can represent: [0xfffffffffffff000, 0x10000000000000000)",
		},
		{
			name:     "Straddle unrepresentable",
			gpaStart: 0xfffffffff000,
			size:     oabi.PageSize,
			product:  sgpb.SevProduct_SEV_PRODUCT_MILAN,
		},
		{
			name:     "Unrepresentable end",
			gpaStart: 0xfffffffff000,
			size:     oabi.PageSize * 2,
			product:  sgpb.SevProduct_SEV_PRODUCT_MILAN,
			wantErr:  "address range is larger than the product can represent: [0xfffffffff000, 0x1000000001000)",
		},
		{
			name:     "Unrepresentable end representable on Genoa",
			gpaStart: 0xfffffffff000,
			size:     oabi.PageSize * 2,
			product:  sgpb.SevProduct_SEV_PRODUCT_GENOA,
		},
	}
	for _, tc := range tests {
		m := SnpMeasurement{Product: tc.product}
		if err := m.ZeroContentUpdate(tc.gpaStart, tc.size, PageTypeZero); !match.Error(err, tc.wantErr) {
			t.Errorf("%s: ZeroContentUpdate(0x%x, %v, PageTypeZero) = %v, want %s", tc.name, tc.gpaStart, tc.size, err, tc.wantErr)
		}
	}
}

func TestDoReserved(t *testing.T) {
	makeDirty := func(size int) []byte {
		result := make([]byte, size)
		for i := range result {
			result[i] = 0xff
		}
		return result
	}
	tests := []struct {
		name       string
		protobytes []byte
		data       []byte
		lo         int
		hi         int
		wantErr    string
		want       []byte
	}{
		{
			name: "emptyOkay",
			data: make([]byte, 16),
			lo:   0,
			hi:   2,
		},
		{
			name:       "wrongSize",
			data:       make([]byte, 16),
			protobytes: make([]byte, 1),
			lo:         0,
			hi:         2,
			wantErr:    "field 'wrongSize' for byte range 0x0:0x2 is not the same size: 1",
		},
		{
			name:       "really_reserved",
			protobytes: makeDirty(8),
			data:       makeDirty(8),
			lo:         9,
			hi:         17,
			wantErr:    "reserved field 'really_reserved' has non-zero byte at index 0x0 (VMSA index 0x9)",
		},
		{
			name:       "cleared",
			protobytes: make([]byte, 3),
			data:       makeDirty(8),
			lo:         3,
			hi:         6,
			wantErr:    "",
			want:       []byte{0xff, 0xff, 0xff, 0, 0, 0, 0xff, 0xff},
		},
	}
	for _, tc := range tests {
		err := doReserved(tc.name, tc.protobytes, tc.data, tc.lo, tc.hi)
		if !match.Error(err, tc.wantErr) {
			var wantErrFormatted error
			if tc.wantErr != "" {
				wantErrFormatted = fmt.Errorf(tc.wantErr)
			}
			t.Errorf("doReserved(%s, %v, %v, %d, %d) did not error as expected. Got %v, want %v", tc.name, tc.protobytes, tc.data, tc.lo, tc.hi, err, wantErrFormatted)
		}
	}
}

