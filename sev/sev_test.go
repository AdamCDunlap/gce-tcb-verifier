func TestPutVmsa(t *testing.T) {
	tests := []struct {
		name       string
		createVmsa func() (*spb.VmcbSaveArea, error)
		want       []byte
		wantErr    string
	}{
		{
			name: "bsp",
			createVmsa: func() (*spb.VmcbSaveArea, error) {
				bspArr, err := prepareVmsas(&LaunchOptions{Vcpus: 1, Product: sgpb.SevProduct_SEV_PRODUCT_MILAN}, nil)
				if err != nil {
					return nil, fmt.Errorf("prepareVmsas(&{LaunchOptions{Vcpus: 1, Product: Milan), nil) errored unexpectedly: %v", err)
				}
				return bspArr[0], nil
			},
			want: vmsaBytes[:],
		},
		{
			name: "empty",
			createVmsa: func() (*spb.VmcbSaveArea, error) {
				return &spb.VmcbSaveArea{}, nil
			},
			want: make([]byte, 4096),
		},
	}
	for _, tc := range tests {
		// Check that PutVmsa of the VmsaV1 proto matches the expected byte representation.
		vmsa, err := tc.createVmsa()
		if err != nil {
			t.Fatal(err)
		}
		var bspBytes [4096]byte
		if err := PutVmsa(vmsa, bspBytes[:]); !match.Error(err, tc.wantErr) {
			t.Fatalf("PutVmsa(bspArr[0], _) error: %v, want %s", err, tc.wantErr)
		} else if diff := cmp.Diff(bspBytes[:], tc.want); diff != "" {
			t.Errorf("PutVmsa(%v) differed from expected BSP binary: %s", vmsa, diff)
		}
	}
}

