func TestUpdateData(t *testing.T) {
	data := make([]byte, oabi.PageSize*3)
	bumper := []byte("LGTMLGTMLGTMLGTM")
	copy(data, bumper)
	copy(data[len(data)-len(bumper):], bumper)
	m := SnpMeasurement{}
	gpaStart := uint64(0)
	if err := m.Update(gpaStart, data, PageTypeNormal); err != nil {
		t.Errorf("Update(0x%x, %v, PageTypeNormal) failed unexpectedly: %v", gpaStart, data, err)
	}

	// We generate the hash below with this python snippet.
	// <code>
	// ```python
	// import hashlib, struct
	// Sha384Len = 48
	// digest_cur = b'\0' * Sha384Len
	// contents = b'LGTM' * 4 + b'\0' * ((4096*3) - (16*2)) + b'LGTM' * 4
	// # Little endian uint16_t for 112 is 0x70, 0x0.
	// len_type_reserved = b'\x70\x00\x01\x00\x00\x00\x00\x00'
	// gpa = 0
	// print(len(contents))
	// for i in range(int(len(contents)/4096)):
	// 	contents_digest = hashlib.sha384(contents[i*4096:(i+1)*4096]).digest()
	// 	gpa_bytes = struct.pack('<Q', gpa)
	// 	page_info = digest_cur + contents_digest + len_type_reserved + gpa_bytes
	// 	digest_cur = hashlib.sha384(page_info).digest()
	// 	gpa += 4096

	// print(', '.join([hex(b) for b in digest_cur]))
	// ```
	// </code>
	want := []byte{
		0xcb, 0xa1, 0xcd, 0x76, 0x7c, 0x33, 0xb3, 0xf, 0x96, 0x27, 0x18, 0x37, 0xdb, 0xba, 0xe4, 0x8,
		0x1a, 0x94, 0x54, 0x64, 0xa5, 0xaf, 0x39, 0x7b, 0x9b, 0x3, 0x36, 0xb5, 0xd8, 0x75, 0x9e, 0xe8,
		0x8c, 0xc6, 0xb6, 0x2c, 0x5, 0xd9, 0x15, 0xc4, 0x42, 0x4a, 0x9a, 0xc5, 0x74, 0x74, 0x37, 0xd4}
	if !bytes.Equal(m.Digest[:], want) {
		t.Errorf("Digest = %v, want %v", m.Digest, want)
	}
}

